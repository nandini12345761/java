package nandini;

public class demo1 {

	public static void main(String[] args) {
		int i=99;
		double d=125.78;
		System.out.println("d="+d);
		System.out.println("i="+i);
		System.out.println("After primitive type conversions");
		i=(int)d;
		System.out.println("d="+d);
		System.out.println("i="+i);

	}

}
